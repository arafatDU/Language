// while loop

let i = 0;
while (i <= 10) {
  console.log(i);
  i++;
}

const myArray = ["ironman", "thor", "hulk", "captain", "blackwidow"];
let j = 0;
while (j < myArray.length) {
  console.log(myArray[j]);
  j++;
}


// do while loop
let k = 11;
do {
  console.log(k); // must run at least once
  k++;
} while (k <= 5);